    @test rob_198([1, 2, 3, 1]) == 4
    @test rob_198([2, 7, 9, 3, 1]) == 12
    @test rob_198([
        40, 28, 8, 40, 32, 49, 31, 39, 11, 24, 3, 37, 10, 16, 23, 33, 3, 48, 41, 8
    ]) == 334
    @test rob_198([
        29, 38, 35, 20, 20, 41, 38, 10, 17, 14, 32, 14, 21, 31, 15, 13, 38, 19, 50, 43
    ]) == 295
